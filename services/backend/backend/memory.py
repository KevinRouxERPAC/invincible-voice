# Prefix length for naive stemming. Tokens of 6+ characters are reduced to
# their first 5 characters so that "jardin", "jardinage", "jardiner" all
# collapse to "jardi" — a cheap way to match word families without a
# dependency on a full stemmer. 5 is long enough to stay discriminative
# ("tomat" vs "terre") but short enough to unify inflections.
_STEM_PREFIX = 5
_STEM_MIN_LEN = 6


def _tokenize(text: str) -> set[str]:
    """Lowercase, alphanumeric tokens, stopwords removed, lightly stemmed.

    Stemming is prefix-based (first 5 chars for words of 6+ chars) so word
    families ("jardin" / "jardinage") match — critical for relevance between
    French conversations that may use different forms of the same root.
    """
    tokens: set[str] = set()
    current: list[str] = []
    for char in text.lower():
        if char.isalnum():
            current.append(char)
        else:
            if current:
                token = "".join(current)
                if len(token) >= _MIN_TOKEN_LEN and token not in _STOPWORDS:
                    if len(token) >= _STEM_MIN_LEN:
                        token = token[:_STEM_PREFIX]
                    tokens.add(token)
                current = []
    if current:
        token = "".join(current)
        if len(token) >= _MIN_TOKEN_LEN and token not in _STOPWORDS:
            if len(token) >= _STEM_MIN_LEN:
                token = token[:_STEM_PREFIX]
            tokens.add(token)
    return tokens


def _conversation_tokens(conversation) -> set[str]:
    """All meaningful tokens across a conversation's messages."""
    tokens: set[str] = set()
    for message in conversation.messages:
        tokens |= _tokenize(message.content)
    return tokens


def select_relevant_past_conversations(
    conversations: list,
    current_conversation,
    max_count: int,
) -> list:
    """Pick the most topically-relevant past conversations for the current one.

    Always excludes the current conversation itself. Returns up to `max_count`
    conversations ordered by descending relevance; ties break to recency.

    The goal is "same knowledge, same tone": if the user is talking about
    gardening today, a past gardening conversation is more useful than a
    more recent but unrelated one, even if both are within the chronological
    window. We still respect recency as a tie-breaker so very old
    conversations don't dominate.
    """
    if not conversations or max_count <= 0:
        return []

    current_tokens = _conversation_tokens(current_conversation)
    if not current_tokens:
        # No signal to match on: fall back to pure recency.
        return [c for c in conversations[-(max_count + 1) : -1] if len(c.messages) > 0][
            :max_count
        ]

    # Score every other conversation by token overlap with the current one.
    # We iterate oldest-to-newest so equal scores keep the more recent one.
    scored: list[tuple[float, int, object]] = []
    for index, conversation in enumerate(conversations):
        if conversation is current_conversation or len(conversation.messages) == 0:
            continue
        tokens = _conversation_tokens(conversation)
        if not tokens:
            continue
        overlap = len(current_tokens & tokens)
        union = len(current_tokens | tokens)
        score = overlap / union if union else 0.0
        # Only keep conversations that share at least one meaningful token;
        # a 0-overlap conversation adds noise, not context.
        if score > 0.0:
            scored.append((score, index, conversation))

    scored.sort(key=lambda item: (-item[0], -item[1]))
    selected = [c for _, _, c in scored[:max_count]]
    # If relevance yielded fewer than asked, top up with the most recent
    # non-empty conversations not already selected.
    if len(selected) < max_count:
        selected_ids = {id(c) for c in selected}
        for conversation in reversed(conversations):
            if len(selected) >= max_count:
                break
            if (
                conversation is current_conversation
                or len(conversation.messages) == 0
                or id(conversation) in selected_ids
            ):
                continue
            selected.append(conversation)
            selected_ids.add(id(conversation))
    return selected
